import 'package:flutter/material.dart';

class ThemeBlueColors {
  static Color primary([double opacity = 1.0]) =>
      const Color(0x0000456b).withOpacity(opacity);

  static Color primaryLight([double opacity = 1.0]) =>
      const Color(0x0012567d).withOpacity(opacity);

  static Color secondary([double opacity = 1.0]) =>
      const Color(0x0024668e).withOpacity(opacity);

  static Color secondaryLight([double opacity = 1.0]) =>
      const Color(0x003677a0).withOpacity(opacity);

  static Color tertiary([double opacity = 1.0]) =>
      const Color(0x004887b1).withOpacity(opacity);
}
